const oracledb = require('oracledb');
const fs = require('fs');
const path = require('path');

const config = {
    user: process.env.ORACLE_USER || 'system',
    password: process.env.ORACLE_PASSWORD || '123456',
    connectString: process.env.ORACLE_CONNECTION_STRING || 'localhost:1521/XE'
};

async function createTable() {
    let connection;
    try {
        // 建立資料庫連接
        connection = await oracledb.getConnection(config);
        console.log('成功連接到 Oracle 資料庫');

        // 建立資料表
        const createTableSQL = `
            CREATE TABLE layout_weekly_weights (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                layout_id NUMBER NOT NULL,
                week VARCHAR2(10) NOT NULL,
                weight NUMBER(5,2) NOT NULL,
                updated_at TIMESTAMP DEFAULT SYSTIMESTAMP,
                updated_by VARCHAR2(100),
                role VARCHAR2(50),
                version NUMBER DEFAULT 1,
                CONSTRAINT fk_layout_weekly_weights_layout 
                    FOREIGN KEY (layout_id) 
                    REFERENCES layout_tasks(id)
            )
        `;

        // 建立索引
        const createIndex1SQL = `
            CREATE INDEX idx_layout_weekly_weights_layout_week 
            ON layout_weekly_weights(layout_id, week)
        `;

        const createIndex2SQL = `
            CREATE INDEX idx_layout_weekly_weights_week 
            ON layout_weekly_weights(week)
        `;

        // 執行 SQL 語句
        await connection.execute(createTableSQL);
        console.log('成功建立 layout_weekly_weights 資料表');

        await connection.execute(createIndex1SQL);
        console.log('成功建立第一個索引');

        await connection.execute(createIndex2SQL);
        console.log('成功建立第二個索引');

        await connection.commit();
        console.log('所有操作已完成');

    } catch (err) {
        console.error('發生錯誤:', err);
        if (connection) {
            await connection.rollback();
        }
    } finally {
        if (connection) {
            try {
                await connection.close();
                console.log('資料庫連接已關閉');
            } catch (err) {
                console.error('關閉連接時發生錯誤:', err);
            }
        }
    }
}

// 執行腳本
createTable().catch(console.error); 